const numero = 5;
const texto = "5";

// == (Comparação imolicita - só compara o valor)
console.log(numero == texto);

// === (Comparação explicita - compara o valor e o tipo de dado)
console.log(numero === texto); 

// Typeof = Pergunta qual tipo de dado está sendo armazenado dentro da variavel
console.log(typeof numero);
console.log(typeof texto);
